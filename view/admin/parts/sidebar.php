	<li><u>Availability time</u>:<span class="br"></span>
		The format is <code>timezone, day(s), hour-start, hour-end</code>. Day start from monday (1). So if you want to set availability day is monday to friday, use 5. If it's monday to saturday, use 6.<span class="br"></span>
		E.g.: <code>+7,5,9,17</code> (GMT+7, Monday to Friday, start from 9am to 5pm)<span class="br"></span>
		Note: This will apply to all numbers and the available day can not be custom (e.g.: monday to thursday and saturday only) since this feature only use a simple (and only few lines of) vanilla js.
	</li>
